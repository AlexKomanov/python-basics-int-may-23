number = 10

print(f"number = {number}")
print("number = ", number)

number = number + 1
print(f"\nnumber = number + 1 \n---> number =  {number}")

# ################  +=  ###################

number += 1  # number++  ++number Increment
print(f"\nnumber += 1 \n---> number =  {number}")

# ################  -=  ###################

number -= 1
print(f"\nnumber -= 1 \n---> number =  {number}")

# ################  *=  ###################

number *= 5
print(f"\nnumber *= 5 \n---> number =  {number}")

# ################  /=  ###################

number /= 2
print(f"\nnumber /= 2 \n---> number =  {number}")

# ################  %=  ###################

number %= 10
print(f"\nnumber %= 10 \n---> number =  {number}")

# ################  //=  ###################

number //= 2
print(f"\nnumber //= 2 \n---> number =  {number}")

# ################  //=  ###################

number **= 5
print(f"\nnumber **= 5 \n---> number =  {number}")
